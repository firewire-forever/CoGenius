def is_windows_os(os_image: Optional[str]) -> bool:
    """Check if the OS is Windows based on image name."""
    if not os_image:
        return False
    os_lower = os_image.lower().strip()
    # Check exact match or partial match for Windows variants
    return os_lower in OS_USER_MAPPING and os_lower.startswith('win') or 'win' in os_lower


def get_ansible_user(os_image: Optional[str]) -> str:
    """Get the appropriate Ansible username for the OS."""
    if not os_image:
        return 'ubuntu'
    os_lower = os_image.lower().strip()
    # Direct lookup
    if os_lower in OS_USER_MAPPING:
        return OS_USER_MAPPING[os_lower]
    # Check for Windows variants
    if 'win' in os_lower:
        return 'Admin'
    # Default to ubuntu
    return 'ubuntu'

