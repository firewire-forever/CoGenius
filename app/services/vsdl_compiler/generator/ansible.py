class AnsibleGenerator:
    """
    Generates Ansible playbooks from VSDL AST.
    Uses Software Registry for intelligent software installation.
    """

    def __init__(self, llm_client=None):
        self.inventory_template = Template(INVENTORY_TEMPLATE)
        self.site_template = Template(SITE_PLAYBOOK_TEMPLATE)
        self.common_roles_template = Template(COMMON_ROLES_MAIN_TEMPLATE)
        # Initialize software registry and LLM generator
        self.software_registry = SoftwareRegistry()
        self.llm_generator = LLMInstallGenerator(llm_client)
        # Note: INSTALL_SOFTWARE_TASK_TEMPLATE is used directly as a raw string

    def generate(self, scenario: Scenario, node_ips: Optional[Dict[str, str]] = None) -> Dict[str, str]:
        """
        Generate Ansible files from a VSDL scenario.

        Args:
            scenario: The VSDL scenario AST
            node_ips: Optional mapping of node names to IP addresses

        Returns:
            Dict mapping filename to content
        """
        if not node_ips:
            node_ips = self._extract_node_ips(scenario)

        # Build groups
        groups = self._build_groups(scenario, node_ips)

        # Build vulnerability-to-nodes mapping
        vuln_nodes = {}
        for vuln in scenario.vulnerabilities:
            if vuln.hosted_on_node:
                if vuln.name not in vuln_nodes:
                    vuln_nodes[vuln.name] = []
                vuln_nodes[vuln.name].append(vuln.hosted_on_node)

        # Generate files
        files = {
            'inventory.ini': self._generate_inventory(scenario, groups, vuln_nodes, node_ips),
            'site.yml': self._generate_site_playbook(scenario, vuln_nodes),
            'roles/common/tasks/main.yml': self._generate_common_role(),
            'tasks/install_software.yml': self._generate_install_software_task(),
        }

        # Generate role-specific playbooks for each software
        software_roles = self._generate_software_roles(scenario)
        for role_path, content in software_roles.items():
            files[role_path] = content

        return files

    def _extract_node_ips(self, scenario: Scenario) -> Dict[str, str]:
        """Extract node IPs from network definitions"""
        node_ips = {}

        for network in scenario.networks:
            for conn in network.connections:
                # Check if it's a node (not another network)
                if conn.node_name in [n.name for n in scenario.nodes]:
                    if conn.ip_address:
                        node_ips[conn.node_name] = conn.ip_address

        return node_ips

    def _build_groups(self, scenario: Scenario, node_ips: Dict[str, str]) -> Dict[str, List[Dict]]:
        """Build inventory groups with OS-specific usernames"""
        groups = {
            'all': []
        }

        # Build node list with OS-specific usernames
        for node in scenario.nodes:
            node_ip = node_ips.get(node.name, 'unknown')
            os_type = (node.os_image or 'ubuntu').lower()
            # Get username based on OS type, default to 'ubuntu'
            ansible_user = get_ansible_user(node.os_image)

            node_info = {
                'name': node.name,
                'ip': node_ip,
                'os': os_type,
                'ansible_user': ansible_user
            }
            groups['all'].append(node_info)

        # Group by OS
        os_groups = {}
        for node in scenario.nodes:
            os_name = (node.os_image or 'unknown').lower()
            if os_name not in os_groups:
                os_groups[os_name] = []

            node_ip = node_ips.get(node.name, 'unknown')
            ansible_user = get_ansible_user(node.os_image)

            os_groups[os_name].append({
                'name': node.name,
                'ip': node_ip,
                'os': os_name,
                'ansible_user': ansible_user
            })
        groups.update(os_groups)

        # Group by vulnerability
        for vuln in scenario.vulnerabilities:
            group_name = f'vulnerability_{vuln.name}'
            if vuln.hosted_on_node:
                # Find the node to get its OS type
                node_obj = next((n for n in scenario.nodes if n.name == vuln.hosted_on_node), None)
                os_type = (node_obj.os_image or 'ubuntu').lower() if node_obj else 'ubuntu'
                ansible_user = get_ansible_user(node_obj.os_image) if node_obj else 'ubuntu'

                ip = node_ips.get(vuln.hosted_on_node, 'unknown')
                groups[group_name] = [{
                    'name': vuln.hosted_on_node,
                    'ip': ip,
                    'os': os_type,
                    'ansible_user': ansible_user
                }]

        return groups

    def _generate_inventory(self, scenario: Scenario, groups: Dict,
                            vuln_nodes: Dict, node_ips: Dict[str, str]) -> str:
        """Generate inventory.ini content"""
        return self.inventory_template.render(
            scenario=scenario,
            groups=groups,
            vulnerabilities=scenario.vulnerabilities,
            vuln_nodes=vuln_nodes
        )

    def _generate_site_playbook(self, scenario: Scenario, vuln_nodes: Dict) -> str:
        """Generate site.yml content"""
        # Filter nodes that have IPs
        nodes_with_ips = []
        for node in scenario.nodes:
            nodes_with_ips.append(node)

        return self.site_template.render(
            scenario=scenario,
            nodes=nodes_with_ips,
            vulnerabilities=scenario.vulnerabilities,
            vuln_nodes=vuln_nodes
        )

    def _generate_common_role(self) -> str:
        """Generate common role tasks"""
        return self.common_roles_template.render()

    def _generate_install_software_task(self) -> str:
        """Generate software installation task"""
        # Return raw content - software_item is a runtime variable in Ansible
        # Do not use Jinja2 render() as it would try to resolve software_item
        return INSTALL_SOFTWARE_TASK_TEMPLATE

    def _generate_software_roles(self, scenario: Scenario) -> Dict[str, str]:
        """Generate role-specific playbooks for software"""
        roles = {}
        generated_software = set()

        for node in scenario.nodes:
            for sw in node.software_mounts:
                sw_name = sw.name.lower()
                if sw_name in generated_software:
                    continue
                generated_software.add(sw_name)

                # Generate a minimal role
                roles[f'roles/{sw_name}/tasks/main.yml'] = self._generate_software_role(sw)

        return roles

    def _generate_software_role(self, software: SoftwareDependency) -> str:
        """Generate a role for a specific software using the knowledge base"""
        sw_name = software.name.lower()

        # Query the software registry
        sw_info = self.software_registry.get_install_info(sw_name, software.version)

        # If not found in knowledge base, try LLM generation
        if sw_info.get('needs_llm_generation'):
            sw_info = self.llm_generator.generate_install_script(
                software_name=sw_name,
                version=software.version,
                os_type="ubuntu",
                offline_mode=False
            )

        # Generate role content based on install type
        install_type = sw_info.get('install_type', 'apt')
        package = sw_info.get('package', sw_name)
        verify_cmd = sw_info.get('verify_cmd', f"which {sw_name}")
        install_script = sw_info.get('install_script')
        dependencies = sw_info.get('dependencies', [])
        note = sw_info.get('note', '')
        generated_by_llm = sw_info.get('generated_by_llm', False)

        role_content = f"""---
# Role: {software.name}
# Generated by VSDL Python Compiler
# Install type: {install_type}
# Package mapping: {software.name} -> {package}
"""

        if generated_by_llm:
            role_content += "# Note: Generated by LLM (not in knowledge base)\n"

        role_content += f"""
- name: Install {software.name}
  block:
"""

        if install_type == 'apt':
            role_content += self._generate_apt_tasks(software.name, package, software.version)
        elif install_type == 'script' and install_script:
            role_content += self._generate_script_tasks(software.name, install_script)
        elif install_type == 'manual':
            role_content += self._generate_manual_tasks(software.name, note or "Requires manual installation")
        else:
            # Fallback to apt
            role_content += self._generate_apt_tasks(software.name, package, software.version)

        role_content += f"""
    - name: Verify {software.name} installation
      command: "{verify_cmd}"
      register: install_check
      changed_when: false
      ignore_errors: yes

    - name: Installation status
      debug:
        msg: |
          {software.name} installation:
          - Install type: {install_type}
          - Package: {package}
          - Status: {{ 'INSTALLED' if install_check.rc == 0 else 'NEEDS MANUAL SETUP' }}
"""

        # Add dependencies if any
        if dependencies:
            role_content += """
    - name: Install dependencies
      apt:
        name:
"""
            for dep in dependencies:
                dep_info = self.software_registry.get_install_info(dep)
                dep_package = dep_info.get('package', dep)
                role_content += f'          - {dep_package}\n'

        # Add configuration tasks
        if software.config:
            role_content += f"""
    - name: Create configuration directory
      file:
        path: /etc/{sw_name}
        state: directory
        mode: '0755'

    - name: Apply configuration
      copy:
        dest: /etc/{sw_name}/vsdl_config.conf
        content: |
"""
            for key, value in software.config.items():
                role_content += f'          {key}={value}\n'

        return role_content

    def _generate_apt_tasks(self, name: str, package: str, version: str = None) -> str:
        """Generate apt installation tasks"""
        tasks = f"""    - name: Install {name} via apt
      apt:
        name: "{package}"
        state: present
        update_cache: true
      ignore_errors: yes
      register: apt_result
"""
        if version and version != 'latest':
            tasks += f"""
    - name: Install specific version {version}
      apt:
        name: "{package}={version}"
        state: present
        update_cache: true
      ignore_errors: yes
"""
        return tasks

    def _generate_script_tasks(self, name: str, script: str) -> str:
        """Generate script-based installation tasks"""
        # Indent the script properly for YAML
        indented_script = '\n'.join(' ' * 8 + line for line in script.strip().split('\n'))
        return f"""    - name: Install {name} via script
      shell: |
{indented_script}
      args:
        executable: /bin/bash
      ignore_errors: yes
"""

    def _generate_manual_tasks(self, name: str, note: str) -> str:
        """Generate manual installation notice"""
        return f"""    - name: {name} requires manual installation
      debug:
        msg: |
          Software: {name}
          Note: {note}
          Please install manually or prepare offline resources.
"""